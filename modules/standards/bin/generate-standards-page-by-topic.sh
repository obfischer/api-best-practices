#!/usr/bin/env bash

(( $# == 2 )) || { echo "${BASH_SOURCE[0]}: expected 2 arguments" >&2; return 1; }

set -e -u -o pipefail

SCRIPT_DIR=$( cd -- "$( dirname -- "${BASH_SOURCE[0]}" )" &> /dev/null && pwd )

# shellcheck disable=SC1091
source "${SCRIPT_DIR}/functions.sh"

source_file="${1:-}"
output_file="${2:-}"

echo > "${output_file}"
(cat data/http-related-standards.yaml | convert_yaml_to_json | jq -r ".standards.[].topics.[]" | sort | uniq ) | while IFS= read -r topic; do
  echo "=== ${topic}" >> "${output_file}"

  {
    cat "${source_file}" \
      | convert_yaml_to_json \
      | filter_by_field_and_value "topics.[]" "${topic}" \
      | generate_list_of_links "${topic}"

    echo
  } >> "${output_file}"
done

echo "// Generated by ${BASH_SOURCE[0]}" >> "${output_file}"
