#!/usr/bin/env bash

(( $# == 2 )) || { echo "${BASH_SOURCE[0]}: expected 2 arguments" >&2; return 1; }

set -e -u -o pipefail

SCRIPT_DIR=$( cd -- "$( dirname -- "${BASH_SOURCE[0]}" )" &> /dev/null && pwd )

# shellcheck disable=SC1091
source "${SCRIPT_DIR}/functions.sh"

source_file="${1:-}"
output_file="${2:-}"

echo > "${output_file}"
for year in $( cat "${source_file}" | convert_yaml_to_json | jq .  |  jq -r ".standards.[].year" | sort -r | uniq ); do
  {
    echo "=== ${year}"
    cat "${source_file}" \
      | convert_yaml_to_json \
      | filter_by_field_and_value year "${year}" \
      | generate_list_of_links "year"

    echo
  } >> "${output_file}"
done

echo "// Generated by ${BASH_SOURCE[0]}" >> "${output_file}"
