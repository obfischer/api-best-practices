#!/usr/bin/env bash

(( $# == 2 )) || { echo "${BASH_SOURCE[0]}: expected 2 arguments" >&2; return 1; }

set -e -u -o pipefail

SCRIPT_DIR=$( cd -- "$( dirname -- "${BASH_SOURCE[0]}" )" &> /dev/null && pwd )

# shellcheck disable=SC1091
source "${SCRIPT_DIR}/functions.sh"

source_file="${1:-}"
target_dir="${2:-}"

if [[ -z "${source_file}" || -z "${target_dir}" ]]; then
  echo "Usage: $0 <source_file> <target_dir>"
  exit 1
fi

for asciidocfile in $( cat "${source_file}" | convert_yaml_to_json | jq .  |  jq -r ".standards.[].asciidocfile" ); do
  target_file=${target_dir}/${asciidocfile}
  echo "Generating ${target_file}"

  standard_description=$(
    cat "${source_file}" \
      | convert_yaml_to_json \
      | filter_by_field_and_value "asciidocfile" "${asciidocfile}" \
      | jq .[]
  )

  anker_prefix="$(echo "${standard_description}" | jq -r .asciidocfile)"

  cat > "${target_file}" <<EOT
= $(echo "${standard_description}" | jq -r .title)

== Abstract from the document or the project

$(echo "${standard_description}" | jq -r .description)

== Source

* $(echo "${standard_description}" | jq -r .url)[^]

== Backlinks for this Standard

* xref:standards-by-title-of-publication.adoc#${anker_prefix}_title[Back to standards sorted by title]
* xref:standards-by-year-of-publication.adoc#${anker_prefix}_year[Back to standards sorted by year]
* xref:standards-by-topic.adoc[Back to the entry in the list of standards sorted by topic]

$(
  while IFS= read -r topic; do
    display_name=${topic}
    # shellcheck disable=SC2001
    anker=$(echo "${topic}" | sed 's/[[:space:]]/_/g')

    echo "** xref:standards-by-topic.adoc#${anker_prefix}_${anker}[${display_name}]"
  done < <(echo "${standard_description}" | jq -r '.topics[]')
)

// generated by ${BASH_SOURCE[0]}

EOT

done
